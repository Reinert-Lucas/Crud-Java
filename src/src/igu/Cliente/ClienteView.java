    public void cargarAutos(int cliente_id) {
        dtm.setRowCount(0);
        try (Connection conn = Conexion.getConexion()) {
            String autosQuery = "SELECT * FROM vehiculos WHERE propietario_id = ? AND estado = 'disp'";
            PreparedStatement pst = conn.prepareStatement(autosQuery);
            pst.setInt(1, ContenedorDatos.getInstance().getIdUser());

            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                dtm.addRow(new Object[]{
                    rs.getString("patente"),
                    rs.getString("marca"),
                    rs.getString("modelo"),
                    rs.getInt("anio")
                });
            }
            System.out.println("Autos actualizados");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar los autos del cliente: " + e.getMessage());
        }
    }

