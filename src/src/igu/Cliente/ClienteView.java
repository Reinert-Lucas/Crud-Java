    private void EliminarAutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarAutoActionPerformed
        // TODO add your handling code here:
        String patenteAuto = JOptionPane.showInputDialog(this, "Ingrese la Patente del Auto a Eliminar");
        if (patenteAuto != null && !patenteAuto.isEmpty()) {
            try (Connection conn = Conexion.getConexion()) {
                String deleteAutoQuery = "UPDATE vehiculos SET estado = 'borrado' WHERE patente = ?";
                PreparedStatement pst = conn.prepareStatement(deleteAutoQuery);
                pst.setString(1, patenteAuto);
                int rs = pst.executeUpdate();
                if (rs > 0) {
                    JOptionPane.showMessageDialog(this, "Auto Eliminado Exitosamente");
                    cargarAutos(ContenedorDatos.getInstance().getIdUser());
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo eliminar el auto.");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Por favor ingrese una Patente Válida.");
            }
        }
    }//GEN-LAST:event_EliminarAutoActionPerformed

